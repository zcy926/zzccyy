              <b>三部门加强小额担保贷款财政贴息资金管理</b>
              <span>发布时间：2014-03-04 来源：市场部 作者：市场部</span>
            </div>
<p>各省、自治区、直辖市、计划单列市财政厅(局)、人力资源社会保障厅(局)，中国人民银行上海总部、各分行、营业管理部、省会(首府)城市中心支行、大连、青岛、宁波、厦门、深圳市中心支行，各有关金融机构：</p>
 
<p>为贯彻落实中央经济工作会议对创业带动就业工作提出的新要求，进一步做好下岗失业人员小额担保贷款(以下简称小额担保贷款)工作，加强财政贴息资金管理，提高财政资金使用效益，现就有关事项通知如下：</p>
 
<p>一、严格执行贴息贷款政策标准</p>
 
<p>(一)小额担保贷款的申请和财政贴息资金的审核拨付，要坚持自主自愿、诚实守信、依法合规的原则。各级财政部门要充分认识到小额担保贷款工作对于促进就业、改善民生的重要意义，切实履行职责，加强财政贴息资金审核，规范政策执行管理。</p>
 
<p>(二)财政贴息资金支持对象按照现行政策执行，具体包括符合规定条件的城镇登记失业人员、就业困难人员(一般指大龄、身有残疾、享受最低生活保障、连续失业一年以上，以及因失去土地等原因难以实现就业的人员)、复员转业退役军人、高校毕业生、刑释解教人员，以及符合规定条件的劳动密集型小企业。上述人员中，对符合规定条件的残疾人、高校毕业生、农村妇女申请小额担保贷款财政贴息资金，可以适度给予重点支持。</p>
 
<p>(三)财政贴息资金支持的小额担保贷款额度为，高校毕业生最高贷款额度10万元，妇女最高贷款额度8万元，其他符合条件的人员最高贷款额度5万元，劳动密集型小企业最高贷款额度200万元。对合伙经营和组织起来就业的，妇女最高人均贷款额度为10万元。</p>
 
<p>(四)财政贴息资金支持的个人小额担保贷款利率为，中国人民银行公布的同期限贷款基准利率的基础上上浮不超过3个百分点。财政贴息资金支持的小额担保贷款期限最长为2年，对展期和逾期的小额担保贷款，财政部门不予贴息。</p>
